// Write a conditional statement that logs "Bring an umbrella" if it's raining, "Wear a jacket" if it's windy, 
// and "Enjoy the weather" if neither condition is true.


let isRaining = true;
let isWindy = false;

if (isRaining) {
    console.log("Bring an umbrella");
} else if (isWindy) {
    console.log("Wear a jacket");
} else {
    console.log("Enjoy the weather!");
}

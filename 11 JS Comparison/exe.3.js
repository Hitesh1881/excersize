// Write a conditional statement that logs "It's a hot day!" if the temperature is above 30 degrees and it's sunny. Otherwise, log "It's a pleasant day."



let temperature = 25;
let isSunny = true;

if (temperature > 30 && isSunny) {
  console.log("It's a hot day!");
} else {
  console.log("It's a pleasant day.");
}
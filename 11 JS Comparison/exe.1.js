// Given two variables:

// let a = 5;
// let b = 8;

// Write a conditional statement that logs whether `a` is greater than, less than, or equal to `b`.



let a = 5;
let b = 8;

if (a > b) {
  console.log(`${a} is greater than ${b}`);
} else if (a < b) {
  console.log(`${a} is less than ${b}`);
} else {
  console.log(`${a} is equal to ${b}`);
}

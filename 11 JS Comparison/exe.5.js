// Given three variables:

// let x = 10;
// let y = "10";
// let z = 5;

// Write conditional statements to compare:
// - If `x` is equal to `y`.
// - If `x` is equal to `z`.
// - If `x` is greater than or equal to `z`.


let x = 10;
let y = "10";
let z = 5;

if (x == y) {
    console.log("x is equal to y");
} else {
    console.log("x is not equal to y");
}

if (x == z) {
    console.log("x is equal to z");
} else {
    console.log("x is not equal to z");
}

if (x >= z) {
    console.log("x is greater than or equal to z");
} else {
    console.log("x is less than z");
}

